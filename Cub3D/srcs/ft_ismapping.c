#include "xu8t.h"

int	ft_ismapping(char x)
{
	if (x == '0' || x == '1'
		|| x == '2' || x == 'N'
		|| x == 'S' || x == 'E'
		|| x == 'W' || x == ' ')
		return (1);
	else
		return (0);
}
